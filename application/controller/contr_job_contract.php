                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.1.</b> Қазақстан Республикасының Еңбек кодексіне сәйкес Жұмыскермен осы Шартты  (оның ішінде біртарапты тәртіпте) өзгертуге, толықтыруға және бұзуға;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.2.</b> өз құзіреті шегінде актілер шығаруға;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.3.</b> осы Шартқа, Жұмыс берушінің бұйрықтарына, Жұмыскердің лауазымдық нұсқаулықтарына, Жұмыс берушінің өзге актілеріне және Қазақстан Республикасының  заңнамасына сәйкес Жұмыскерден  оның еңбектік міндеттерін орындауды талап етуге;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.4.</b> еңбек туралы заңнамада белгіленген тәртіпте Жұмыскерді тәртіптік және материалдық жауапкершілікке тартуға;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.5.</b> Жұмыскердің оған келтірген зиянын өтетуге;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.6.</b> осы Шартқа және Қазақстан Республикасының Еңбек кодексіне сәйкес Жұмыскерді оқытумен байланысты өзінің шығындарын өтетуге;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.7.</b> иеленетін лауазымына сәйкестікке Жұмыскерді мерзімді аттестаттауға, сондай-ақ сақтандыру, зейнетақы заңнамасын, Қазақстан Республикасының басқа нормативтік  құқықтық  актілерін, Жұмыс берушінің  актілері біліуіне Жұмыскерді сынақтан мерзім сайын өткізіп тұруға; аттестаттаудың теріс нәтижелері бойынша Жұмыс беруші Жұмыскерге  қатысты, Қазақстан Республикасының заңнамасымен көзделген тиісті шараларды қолдануға құқылы;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.8.</b> заңмен белгіленген мөлшерден кем емес, тәуліктік шығындарды төлеумен, және заңнамамен қарастырылған өзге шығындарды өтеумен тұрақты жұмыс орнынан тыс жерде еңбек міндеттерін атқару үшін Жұмыскерді қызметтік іс-сапарларға жіберуге;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.9.</b> қажет болған жағдайда Жұмыскерді жыл сайынғы еңбек демалысынан шақырып алуға, бұл ретте тараптар арасындағы уағдаластық бойынша еңбек демалысының пайдаланылмаған  бөлігі ағымдағы ағымдағы жұмыс жылы ішінде беріледі, немесе жұмыскердің қалауы бойынша келесі жұмыс жылындағы еңбек демалысына қосылады, немесе Жұмыскерге Қазақстан Республикасының заңнамасына сәйкес өтемақы төленеді;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.10.</b> заңнамамен белгіленген тәртіпте Жұмыс берушінің  ұйымдық жүйесі шегінде Жұмыскердің мамандығы мен біліктілігін ескерумен оны басқа жұмысқа ауыстыруға; 
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.11.</b> Қазақстан Республикасының қолданыстағы заңнамасымен қарастырылған тәртіпте және мерзімге Жұмыскерді жұмыстан шеттетуге;
                </div>
                <div align="justify" style="text-indent: 10px;"><b>3.1.12.</b> Қазақстан Республикасының қолданыстағы заңнамасымен қарастырылған өзге құқықтарға құқығы бар. 
