                <div align="justify" style="text-indent: 10px;"><b>5.3.</b> Жұмыскерге екі демалыс күнімен бескүндік жұмыс аптасы белгіленеді. Жұмыс күнінің басталуы – 9 сағат 00 минут; аяқталуы – 18 сағат 30 минут. Демалу мен тамақтану үшін үзіліс – 1 сағат 30 минут, 13:00 – ден 14:30 сағатқа дейін, бұл кезді Жұмыскер өзінің қалауы бойынша пайдалануға құқылы.  Жұмыс беруші осы Шартқа өзгерістер енгізбестен, заңнамаға сәйкес Жұмыскердің жұмысының өзге кестесін белгілеуге құқылы.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>5.4.</b> Жұмыскер Қазақстан Республикасының еңбек туралы заңнамасымен көзделген тәртіпте үстеме жұмыстарға тартылуы мүмкін.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>5.5.</b> Жұмыскерге отыз күнтізбелік күн ұзақтығымен жыл сайынғы төленетін еңбек демалысы беріледі. Жыл сайынғы төленетін еңбек демалысы бөліктерге бөлінуі мүмкін. Бұл ретте жыл сайынғы төленетін еңбек демалысының бір бөлігі жұмыскердің еңбек шартында көзделген демалыстың ұзақтығынан кемінде екі күн түзбелік күн болуы керек. Жұмыстың бірінші және одан кейінгі жылдары үшін берілетін жыл сайынғы төленетін еңбек демалысы Жұмыс берушімен бекітілетін, демалыстарды беру кестесіне сәйкес Қазақстан Республикасының заңнамасына сәйкес тараптардың келісімі бойынша беріледі.
               </div>
                <div align="justify" style="text-indent: 10px;"><b>5.6.</b> Демалыстар кестесі өндірістік қажеттілікке байланысты өзгерген жағдайда Жұмыс беруші бұл туралы Жұмыскерге оның кезекті еңбек демалысы басталардан кемінде 2 (екі) апта бұрын дегенде  хабарлауы керек. 
                </div>
                <div align="justify" style="text-indent: 10px;"><b>5.7.</b> Жұмыскердің өтініші бойынша демалыс демалыстардың құрастырылған кестесін бұзумен берілуі мүмкін.
                </div>
               <div align="justify" style="text-indent: 10px;"><b>5.8.</b> Жыл сайынғы еңбек демалысы Жұмыс берушінің  ұсынысы және Жұмыскердің келісімі  бойынша тоқтатылуы мүмкін. Еңбек демалысының пайдаланылмаған бөлігі Тараптар арасындағы уағдаластық бойынша ағымдағы еңбек жұмыс жылы ішінде беріледі, не болмаса Жұмыскердің қалауы бойынша келесі жұмыс жылы ішіндегі еңбек демалысына қосылады, немесе өтемақы төленеді.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>5.9.</b> Жыл сайынғы төленетін еңбек демалысын толықтай немесе бөліп берген жағдайда, Жұмыскерге Қоғамда ең азы 9 (тоғыз) ай жұмыс жағдай жасауы шартымен сауықтыруға жәрдемаақы төленеді.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>5.10.</b> Жүктілікке және босануға, жаңа туған баланы (балаларды) асырап алуға байланысты  демалысты рәсімдеген жұмыскерге орташа жалақыны сақтай отырып, Қазақстан Республикасының міндетті әлеуметтік сақтандыру туралы заңнамасына сәйкес жүктілікке және босануға, жаңа туған баланы (балаларды) асырап алуға байланысты табысынан айырылған жағдайда төленетін әлеуметтік төлем сомасын шегеріп, жүктілікке және босануға, жаңа туған баланы (балаларды) асырап алған демалысқа ақы төлейді.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>5.11.</b>Тараптардың келісімі бойынша Жұмыскердің өтініші негізінде оған жалақының сақталуынсыз қосымша демалас берілуі мүмкін.
                </div>
                <br/>
                <div style="text-align:  center;">
                <strong>6. ШАРТЫҢ ӨЗГЕРУ ЖӘНЕ ТОҚТАТЫЛУ ТӘРТІБІ</strong>
                </div>                                
                <div align="justify" style="text-indent: 10px;"><b>6.1.</b> Осы Шартқа өзгерістер мен толықтырулар енгізу, оның ішінде басқа жұмысқа ауыстыру кезінде ҚР Еңбек кодексінің 33- бабымен көзделген тәртіпте жазбаша түрде тараптармен жүзеге асырылады.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.2.</b> Шарттың шартарын өзгерту туралы ұсынысты Шарттың тараптарының бірі жазбаша түрде береді және екінші тарап оны берген күннен бастап  бес жұмыс күні ішінде қарастырады.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.3.</b> Осы Шарт ҚР Еңбек кодексінің 49-бабымен қарастырылған негіздер бойынша тоқтатылуы мүмкін.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.4.</b> Осы Шарт тоқтатылған жағдайда Жұмыскерге жұмыс уақытын есептеу табеліне сәйкес нақты жұмыс істеген күндері төленеді, және Тараптар арасындағы барлық есептесулер Қазақстан Республикасының заңнамасымен белгіленген  мерзімде жүргізіледі.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.5.</b> Жұмыскердің бастамасы бойынша осы Шартты мерзімінен бұрын бұзылған жағдайда Жұмыскер ҚР Еңбек кодексінің 57-бабының 4-тармағы 2-4)-тармақшасымен қарастырылған жағдайларды қоспағанда, бұл Шартты бұзғанға дейін бір ай бұрын Жұмыс берушіні жазбаша түрде (өз қолымен жазылған өтініш түрінде) хабардар етуге міндетті. 
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.6.</b> Жұмыс беруші өзінің бастамасы бойынша осы Шартты мерзімінен бұрын бұзған жағдайда осы Шартты бұзғанға дейін бір ай ішінде мерзімінен бұрын бұзудың себептерін көрсетумен Жұмыскерге жазбаша түрде хабарлауға міндетті. 
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.7.</b> Осы Шартты Жұмыс берушінің немесе оның құрылымдық бөлімшелерінің таратылуы салдарынан мерзімінен бұрын бұзған жағдайда, сондай-ақ Жұмыс берушінің немесе оның құрылымдық бөлімшелерінің жұмыскерлер санын немесе штатын қысқартқан жағдайда Жұмыскерде Қазақстан Республикасының еңбек заңнамасымен қарастырылған барлық кепілдіктер сақталады.
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.8.</b> Осы Шарт осы Шарттың қолданыс мерзімі ішінде анықталған Жұмыскердің жеткіліксіз біліктілік себебінен иеленіп отырған лауазымына немесе орындайтын жұмысқа сәйкес келмеуі салдарынан Жұмыс берушінің бастамасы бойынша бұзылуы мүмкін (ҚР Еңбек кодексінің 52-бабының 1-тармағы (4-тармақшасы.). Жұмыскердің жеткіліксіз біліктілігін  аттестаттау мен тестілеудің нәтижелері растайды. Жұмыскерді тестілеу сынақ мерзімі аяқталған соң, басқа лауазымға ауыстыру кезінде, лауазымдық міндеттерін тиісті емес дәрежеде орындағаны үшін тәртіптік жаза қолданған жағдайда өткізілуі мүмкін. Аттестаттау мен тестілеудің нәтижелері қанағаттанарлықсыз болған жағдайда Жұмыс беруші осы Шартты біртарапты тәртіпте бұзуға құқылы. 
                </div>
                <div align="justify" style="text-indent: 10px;"><b>6.9.</b> Шартты тоқтату ҚР Еңбек кодексіне сәйкес, Шарттың тоқтатылуы туралы негізді көрсетумен Жұмыс берушінің бұйрығымен ресімделеді. Жұмыскер онымен Шартты тоқтату туралы бұйрықпен танысудан бас тартқан жағдайда  бұйрықта Жұмыскердің бұйрықпен танысу кезінде болған, Жұмыс берушінің Жұмыскерлерінің (кемінде екі) қолтаңбасымен расталатын белгі жасалады.
                </div>
                
